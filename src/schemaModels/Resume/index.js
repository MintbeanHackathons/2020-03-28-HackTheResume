class Resume {
  constructor(
    basics,
    work,
    volunteer,
    education,
    awards,
    publications,
    skills,
    languages,
    interests,
    references
  ) {
    this.basics = basics
    this.work = work
    this.volunteer = volunteer
    this.education = education
    this.awards = awards
    this.publications = publications
    this.skills = skills
    this.languages = languages
    this.interests = interests
    this.references = references
  }
}

export default Resume